#include "3-calc.h"

/**
* op_add - Adds two numbers
*
* @a: first number
*
* @b: secona number
*
* Return: (Always/Success)
*/

int op_add(int a, int b)
{
return (a + b);
}

/**
* op_sub - Subtracts two numbers
*
* @a: first number
*
* @b: secona number
*
* Return: (Always/Success)
*/

int op_sub(int a, int b)
{
return (a - b);
}

/**
* op_mul - Multiplies two numbers
*
* @a: first number
*
* @b: secona number
*
* Return: (Always/Success)
*/

int op_mul(int a, int b)
{
return (a * b);
}

/**
* op_div - Divides two numbers
*
* @a: first number
*
* @b: secona number
*
* Return: (Always/Success)
*/

int op_div(int a, int b)
{
return (a / b);
}

/**
* op_mod - Finds to modulo of two numbers
*
* @a: first number
*
* @b: secona number
*
* Return: (Always/Success)
*/

int op_mod(int a, int b)
{
return (a % b);
}
